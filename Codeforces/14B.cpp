void debugarr(int * a, int n)
{
    printf("[ ");
    for(int i=0;i<n;i++) printf("%d ",a[i]);
    printf("]\n");
}

//Main code begins here
int n,x,a,b,ret;
int g[1005];

int main()
{
	bool f=0;
	cin>>n>>x;
	for(int i=0; i<n; ++i)
	{
		cin>>a>>b;
		for(int j=min(a,b); j<=max(a,b); ++j)
		{
			g[j]++;
		}	
	}
	for(int i=0; i<1005; ++i)
	{
		if(x+i<=1000 && g[x+i]==n) {ret=i; f=1; break;}
		if(x-i>=0 && g[x-i]==n) {ret=i; f=1; break;}
	}
	if(!f) cout<<-1<<endl;
	else cout<<ret<<endl;
	return 0;
}
